
print("Comparison Operators:")
### All operators: ==, !=, <, >, <=, >=
i = 8
print(i == 8)  # Comparing value of i (will return True)
print(i != 8)  # will return False
print(i > 6)  # will return True
print(i < 8)  # will return False
print(i <= 8)  # will return True
print(i >= 10)  # will return False
